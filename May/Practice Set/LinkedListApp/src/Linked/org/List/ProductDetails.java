package Linked.org.List;
import java.util.*;

public class ProductDetails {

	private int id;
	private String mgmonth;
	private String exmonth;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getMgmonth() {
		return mgmonth;
	}
	public void setMgmonth(String mgmonth) {
		this.mgmonth = mgmonth;
	}
	public String getExmonth() {
		return exmonth;
	}
	public void setExmonth(String exmonth) {
		this.exmonth = exmonth;
	}
	
}
